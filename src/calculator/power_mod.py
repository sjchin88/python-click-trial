def powerup(base: int, power: int) -> int:
    """ return base raised to the power

    Args:
        base (int): base of a number
        power (int): power we want to raised to

    Returns:
        int: result of calculation
    """
    return base**power


def addition(val1: int, val2: int) -> int:
    """ Simple addition

    Args:
        val1 (int): value 1
        val2 (int): value 2

    Returns:
        int: result of addition
    """
    return val1 + val2
